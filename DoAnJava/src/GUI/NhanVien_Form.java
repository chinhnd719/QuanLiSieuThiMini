    public boolean timkiemNgay(NhanVien s) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date_timkiem = sdf.format(Date_timkiem.getDate());
        String date = sdf.format(s.getNgayVaoLam());
        if (date.compareTo(date_timkiem) == 0) {
            return true;
        }
        return false;
    }

