    private void tbl_nhanvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_nhanvienMouseClicked
        // TODO add your handling code here:
        int i = tbl_nhanvien.getSelectedRow();
        String date = tbl_nhanvien.getValueAt(i, 3).toString();
        txt_manv.setText(tbl_nhanvien.getValueAt(i, 0).toString());
        txt_tennv.setText(tbl_nhanvien.getValueAt(i, 1).toString());
        txt_diachi.setText(tbl_nhanvien.getValueAt(i, 2).toString());
        try {
            date_ngaysinh.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(date));
            date_vaolam.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(tbl_nhanvien.getValueAt(i, 5).toString()));
        } catch (ParseException ex) {
            Logger.getLogger(NhanVien_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        txt_mucluong.setText(tbl_nhanvien.getValueAt(i, 4).toString());
        txt_sdt.setText(tbl_nhanvien.getValueAt(i, 6).toString());

        if (tbl_nhanvien.getValueAt(i, 7).toString().equals("m")) {
            rb_nam.doClick();
        } else {
            rb_nu.doClick();
        }
        txt_cmnd.setText(tbl_nhanvien.getValueAt(i, 8).toString());

    }//GEN-LAST:event_tbl_nhanvienMouseClicked

    private void bt_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_themActionPerformed
        // TODO add your handling code here:
        NhanVien s = new NhanVien();
        s.setMaNV(txt_manv.getText());
        s.setTenNV(txt_tennv.getText());
        s.setDiaChiNV(txt_diachi.getText());
        s.setCMND(txt_cmnd.getText());
        if (rb_nam.isSelected()) {
            s.setGioiTinh("m");
        } else {
            s.setGioiTinh("f");
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date_ngay = sdf.format(this.date_ngaysinh.getDate());
        String date_ngayvaolam = sdf.format(date_vaolam.getDate());
        try {
            s.setNgaySinh(new SimpleDateFormat("yyyy-MM-dd").parse(date_ngay));
        } catch (ParseException ex) {
            Logger.getLogger(NhanVien_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            s.setNgayVaoLam(new SimpleDateFormat("yyyy-MM-dd").parse(date_ngayvaolam));
        } catch (ParseException ex) {
            Logger.getLogger(NhanVien_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        s.setMucLuong(Integer.parseInt(txt_mucluong.getText()));
        s.setSDT(txt_sdt.getText());
        if (new NhanVienDAO().addNhanVien(s)) {
            JOptionPane.showMessageDialog(rootPane, "Add Success!");
            ListNhanVien.add(s);
            reloadNhanvien();
        } else {
            JOptionPane.showMessageDialog(rootPane, "ID cannot be duplicated!");
        }
        System.out.println(s.getCMND() + s.getDiaChiNV() + s.getGioiTinh() + s.getNgaySinh());

    }//GEN-LAST:event_bt_themActionPerformed

