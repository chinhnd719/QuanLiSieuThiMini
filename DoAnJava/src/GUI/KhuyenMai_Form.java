       public void showTable(){
        int i=1;
        for (KhuyenMai s:list){
            model.addRow(new Object[]{
              i++,s.getMaChuongTrinh(),s.getTenChuongTrinh(),s.getNgayBatDau(),s.getNgayKetThuc(),s.getNoiDungChuongTrinh()
            });
        }
    }
    public void clearTable(){
        for (int i=model.getRowCount()-1;i>=0;i--){
            model.removeRow(i);
        }
    }
     public void showTable1(){
        int i=1;
        for (ChiTietKhuyenMai s:list1){
            model1.addRow(new Object[]{
              i++,s.getMaMatHang(),s.getMaChuongTrinh(),s.getLoaiKhuyenMai(),s.getDiscountValue()
            });
        }
    }
    public void clearTable1(){
        for (int i=model1.getRowCount()-1;i>=0;i--){
            model1.removeRow(i);
        }
    }
