    public void showDBbb(){
        int selectrow=jTable1.getSelectedRow();
        Object[] row= new Object[5];
        String ss=jTable1.getValueAt(selectrow,1).toString();
        clearTable1();
        for (int i=0;i<list1.size();i++){
            if (list1.get(i).getMaChuongTrinh().equals(ss)){
                int k=0;
                k++;
                row[0]=k;
                row[1]=list1.get(i).getMaMatHang();
                row[2]=list1.get(i).getMaMatHang();
                row[3]=list1.get(i).getLoaiKhuyenMai();
                row[4]=list1.get(i).getDiscountValue();
                model1.addRow(row);
            }
        }
    }
