     public void showTable(){
        int i=1;
        for (PhieuNhap s:list){
            model.addRow(new Object[]{
           i++, s.getMaPN(),s.getMaNCC(),s.getMaNV(),s.getNgayNhap(),s.getTongTien()
    });
    }
    }
   
    public void clearTable(){
        for (int i=model.getRowCount()-1;i>=0;i--){
            model.removeRow(i);
        }
    }
    public void showTable1(){
        int i=1;
        for (ChiTietPhieuNhap s:list1){
            model1.addRow(new Object[]{
                i++,s.getMaPN(),s.getMaMatHang(),s.getSoluong(),s.getGiaNhap()
            });
        }
    }
    public void clearTable1(){
        for (int i=model1.getRowCount()-1;i>=0;i--){
            model1.removeRow(i);
        }
    }
    public void showTB(){
        int selectrow=jTable1.getSelectedRow();
        Object[] row= new Object[5];
        String s=jTable1.getValueAt(selectrow,1).toString();
        clearTable1();
        for (int i=0;i<list1.size();i++){
            if (list1.get(i).getMaPN().equals(s)){
                int k=0;
                k++;
                row[0]=k;
                row[1]=list1.get(i).getMaPN();
                row[2]=list1.get(i).getMaMatHang();
                row[3]=list1.get(i).getSoluong();
                row[4]=list1.get(i).getGiaNhap(); 
                model1.addRow(row);
            }
        }
    }
