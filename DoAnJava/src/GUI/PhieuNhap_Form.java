    public void showDB(){
        int selectrow=jTable1.getSelectedRow();     
        
        txMaPhieuNhap.setText(jTable1.getValueAt(selectrow,1).toString());
        txMaNCC.setText(jTable1.getValueAt(selectrow,2).toString());
        txMaNV.setText(jTable1.getValueAt(selectrow,3).toString());
        txTongTien.setText(jTable1.getValueAt(selectrow,5).toString());
        Date date;
        try {
            date = new  SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(selectrow,4));
            dateNgayNhap.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(HoaDon_form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
