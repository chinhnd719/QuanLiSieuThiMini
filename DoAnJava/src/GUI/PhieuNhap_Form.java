         PhieuNhap s=new PhieuNhap();
        SimpleDateFormat sdf=new  SimpleDateFormat("yyyy-MM-dd"); 
        String date=sdf.format(dateNgayNhap.getDate());
        s.setMaPN(txMaPhieuNhap.getText());
        s.setMaNCC(txMaNCC.getText());
        s.setMaNV(txMaNV.getText());
